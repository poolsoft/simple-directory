<h3>Getting Started</h3>
Adding a new listing is simple.  Add the name of the organization or individual to as the listing title, and then fill in as much detail as possible.

All external links are rel="nofollow", so that your directory won't violate Google's Webmaster Guidelines, if you decided to charge for listings.